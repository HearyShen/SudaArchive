#include <stdio.h>

void main()
{
	int m,n,x,y;	//依次定义待输入的两个正整数m和n，以及待输出的最大公约数x和最大公倍数y
	printf("请输入两个正整数m和n\n");
	scanf("%d%d",&m,&n);

	x=m;	//最大公约数一定不大于m,n中任意一个
	y=m;	//最小公倍数一定不小于m,n中任意一个

	while(m%x!=0||n%x!=0)
	{
		x=x-1;	//每次减1，再继续查找
	}
	while(y%m!=0||y%n!=0)
	{
		y=y+1;	//每次加1，再继续查找
	}

	printf("这两个数字的最大公约数为%d\n最小公倍数为%d\n",x,y);
}