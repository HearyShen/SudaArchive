#include <stdio.h>

void main()
{
	double S=0,a=1.0,b=2.0;		//初始化前20项和S,表示数列所需的a,b（a,b都定义为double,避免不同数据类型混合运算）
	int i;		//定义循环变量

	for(i=1;i<=20;i++)
	{
		if(i%2==0)		//数列的偶数项
		{
			S=S+a/b;
			b=b+a;		//为计算下一个奇数项做准备
		}
		else		//数列的奇数项
		{
			S=S+b/a;
			a=a+b;		//为计算下一个偶数项做准备
		}
	}
	printf("这个数列的前20项之和为%lf\n",S);
}