#include <stdio.h>

void main()
{
	int a,l;	//依次定义ASCII码值a，以及用于判别列数的l
	printf("以下是ASCII码为32-126的ASCII字符：\n");

	for(a=32;a<=126;a++)
	{
		printf("%c	",a);	//字符形式输出ASCII数值
		
		l=(a-31)%5;		//每当列数被5整除时，就换行
		if(l==0)
		{
			printf("\n");
		}
	}
}