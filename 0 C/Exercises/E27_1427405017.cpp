#include <stdio.h>
#include <string.h>

void main()
{
	char in[100],out[100];		//定义输入、输出字符数组
	int i,l;					//定义循环变量i，字符数组长度l

	printf("请输入一串字符串，程序将显示其逆序字符串：\n");
	gets(in);
	l=strlen(in);				//测出字符串长度

	for(i=0;i<l;i++)
	{
		out[i]=in[l-1-i];		//利用其数学关系为输出字符数组赋值
	}
	out[l]='\0';				//最后一位要补上\0，使字符数组能结束

	puts(out);
}