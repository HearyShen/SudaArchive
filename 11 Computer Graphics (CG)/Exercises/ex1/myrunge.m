clc;
clear all;
close all;

% 定义原函数并生成若干点
x = -1 : 0.2 : 1;   % n=11，预备差值采样点11个
y = 1./(1+5*x+25*x.^2);
plot(x,y,'o')  % 原函数采样点为o型

% 绘制龙格现象
hold on
m = 10;     % m=n-1=10，此时拟合即插值
 % 计算并绘制10次插值函数（-1~1采样区间，0.01采样精度）
 p = polyfit(x,y,m);
 x1 = -1 : 0.01 : 1;
 y1 = polyval(p,x1);
 plot(x1,y1, 'r','linewidth', 2)   % 差值函数为红色，粗线
 % 计算并绘制原函数参照图形（采样情况同上）
 y2 = 1./(1+5*x1+25*x1.^2);
 plot(x1,y2,'b','linewidth',1)     % 原函数为蓝色，细线

